<h2>
	Paul Labile Pogba
</h2>
<p>Paul Labile Pogba (lahir di Lagny-sur-Marne, Perancis, 15 Maret 1993; umur 23 tahun) adalah seorang pemain sepak bola profesional Perancis yang bermain untuk klub Liga Utama Inggris, Manchester United dan tim nasional Perancis. Ia berposisi sebagai gelandang tengah dan nyaman dalam bermain baik menyerang dan bertahan.[3]

Pogba digambarkan oleh klubnya Manchester United sebagai pemain yang "kuat, terampil, dan kreatif".[4] Setelah ia pindah ke Juventus F.C. ia mendapatkan julukan Il Polpo Paul (merujuk kepada "Paul si Gurita") karena kakinya yang panjang dan terlihat seperti tentakel ketika melakukan tackle ataupun saat berlari.[5]

Pogba telah mewakili negaranya di semua tingkatan umur tim nasional. Bersama tim nasional U-16, dia menjadi kapten bagi negaranya dalam kemenangan di Piala Aegea dan Tournoi du Val-de-Marne, dan pada level tim nasional U-17 ia bermain pada Kejuaraan Sepak Bola U-17 Eropa UEFA 2010. Dia melakukan debut untuk tim nasional senior Perancis pada 22 Maret 2013 dalam kemenangan 3-1 melawan Georgia.</p>

