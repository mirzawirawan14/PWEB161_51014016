<h1>Pasca Karier</h1>
<ul>
	<li>Tahun 2000 Maradona meluncurkan otobiografi nya berjudul Yo Soy El Diego (Sayalah Diego) yang mana langsung menjadi best seller di Argentina. Maradona mempersembahkan sebagian royalti penjualan buku ini kepada "rakyat Kuba dan Fidel"</li>
	<li>Tahun 2001 organisasi Asosiasi Sepak Bola Argentina (AFA) mengusulkan kepada FIFA untuk mempensiunkan nomor punggung 10 sebagai bentuk penghormatan keada Maradona. FIFA tidak mengizinkan hal tersebut.</li>
	<li>Argentinos Juniors menamai stadion mereka Stadion Maradona pada tahun 2003.</li>
	<li>Pada tahun 2004, Maradona hampir meninggal dunia akibat serangan jantung karena overdosis kokain. Setelah keluar dari rumah sakit, ia melakukan operasi perut pada Maret 2005 untuk mengurangi beratnya. Pada Agustus 2005, ia memulai karier baru sebagai pemandu acara talk show La Noche del 10 (Acara malam si nomor 10).</li>
	<li>Pada 2008, Maradona secara mengejutkan terpilih menjadi pelatih kepala Argentina. Pada debutnya sebagai pelatih baru tim Tango, Maradona berhasil membawa timnya menumbangkan Skotlandia 1 - 0 di Glasgow. Skotlandia. Maradona juga menunjuk Javier Mascherano, gelandang Barcelona sebagai kapten baru timnas. Dalam Piala Dunia 2010 di Afrika Selatan, Maradona mengantarkan Argentina melaju sampai babak perempat final di mana Argentina dikalahkan oleh Jerman dengan skor telak 4-0. Maradona kemudian dipecat pada bulan Juli 2010.</li>
</ul>


