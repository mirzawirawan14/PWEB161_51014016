<p>Diego Armando Maradona (lahir di Buenos Aires, Argentina, 30 Oktober 1960; umur 56 tahun) yang lebih dikenal dengan sebutan Maradona adalah mantan pesepak bola legendaris Argentina. Maradona menjadi pelatih timnas Argentina mulai November 2008 sampai Juli 2010. Untuk Argentina Maradona tampil sebanyak 91 kali dan mencetak 34 gol. Maradona termasuk dalam deretan pesepak bola terbaik abad ini bersama dengan Pele, Johan Cruyff dan Christian Vieri</p>

<h1>Kelahiran</h1>
<p>Kelahiran
Maradona lahir dalam kemiskinan pada tanggal 30 Oktober 1960 di Lanús tetapi menghabiskan masa kecilnya di Villa Fiorito. Dia adalah anak ke 4 dari 6 bersaudara.</p>

<h1>Karier Club</h1>
<p>Pada usia 10 tahun bakat sepak bolanya ditemukan oleh pemandu bakat klub Agentinos Juniors. 2 tahun kemudian dia menjadi maskot klub tersebut bernama Los Cebollitas (Bawang Kecil), yang mana dia bertugas untuk menghibur penonton dengan keterampilan sepak bolanya saat jeda pertandingan pada kompetisi divisi utama Argentina, Argentinos Juniors. Bakatnya tercium sampai ke Inggris saat klub Sheffield United mencoba mentransfernya seharga 180.000 poundsterling. Proposal itu kemudian ditolak oleh Argentinos Juniors. Setahun kemudian, ia melakukan debut internasional bersama timnas Argentina. Pada tahun 1981, ia dibeli klub Boca Juniors seharga 1 juta poundsterling di mana ia menjadi juara liga untuk pertama kalinya.</p>

