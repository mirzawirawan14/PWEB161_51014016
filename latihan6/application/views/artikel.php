<h1>Karier International</h1>
<p>Maradona memulai debutnya bersama Argentina pada usia 16 tahun melawan Hongaria pada 27 Februari 1977. Pada usia 18 tahun Maradona berpartisipasi dalam Piala Dunia Junior yang diselenggarakan di Jepang, di mana Argentina sempat berhadapan dengan Indonesia dengan hasil 5-0. Maradona mencetak 2 gol bersama Ramón Díaz yang mencetak hattrick.</p>

